#operasi aritmatika

a = 10
b = 3
# tambah
hasil = a+b
print( a , '+', b , '=', hasil)
# kurang
hasil = a-b
print( a , '-', b , '=', hasil)

# Kali
hasil = a*b
print( a , '*', b , '=', hasil)

#Bagi
hasil = a/b
print( a , '/', b , '=', hasil)

#Eksponen
hasil = a**b
print( a , '**', b , '=', hasil)

#Modulus
hasil = a%b
print( a , '%', b , '=', hasil)

#Floor Division -> Pembulatan kebawah
hasil = a//b
print( a , '//', b , '=', hasil)

# Prioritas Operasi / Operational Precedence
x = 3
y = 2
z = 4
hasil = x ** y * z + x / y - y % z // x
print(x,'**',y,'*',z,'+',x,'/',y,'-',y,'%',z,'//',x,'=',hasil)

